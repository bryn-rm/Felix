async def test_people_query_excludes_automated_and_noreply_senders():
    """The people_rows query must filter category and noreply patterns server-side.

    We verify by inspecting the SQL passed to db.query for the people block.
    """
    svc = PolishService()

    captured: list[str] = []

    async def fake_query(sql, *args, **kwargs):
        captured.append(sql)
        return []

    async def fake_query_one(sql, *args, **kwargs):
        return {}

    with patch("app.services.polish_service.db.query", new=fake_query), \
         patch("app.services.polish_service.db.query_one", new=fake_query_one), \
         patch("app.middleware.auth.get_google_credentials",
               new=AsyncMock(side_effect=Exception("no creds in test"))):
        await svc.gather_weekly_context("user-123")

    people_sql = next(
        (s for s in captured if "GROUP BY e.from_email" in s and "ORDER BY n DESC" in s),
        None,
    )
    assert people_sql is not None, "expected the people aggregation query to run"
    assert "category NOT IN ('automated', 'newsletter')" in people_sql
    for pattern in ("noreply", "no-reply", "notifications@", "invitations@",
                    "alerts@", "donotreply"):
        assert pattern in people_sql, f"missing filter for {pattern}"


