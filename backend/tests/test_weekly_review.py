async def test_generate_weekly_review_falls_back_on_claude_failure():
    svc = PolishService()
    fixed_now = datetime(2026, 4, 27, 18, 0, tzinfo=timezone.utc)
    ctx = {
        "user_id":             "u",
        "display_name":        "Bryn",
        "user_timezone":       "Europe/London",
        "since":               fixed_now - timedelta(days=7),
        "until":               fixed_now,
        "next_until":          fixed_now + timedelta(days=7),
        "category_counts":     {"action_required": 2, "fyi": 5, "newsletter": 30},
        "action_required":     [],
        "sent_replies":        [],
        "unresolved":          [],
        "open_followups":      [],
        "follow_ups_resolved": 1,
        "people":              [],
        "past_meetings":       [],
        "next_meetings":       [],
        "stats": {
            "processed_total": 37, "sent_replies": 2,
            "meetings_held": 3, "follow_ups_resolved": 1,
        },
        "stats_line": "stats line",
    }

    with patch.object(svc, "gather_weekly_context", new=AsyncMock(return_value=ctx)), \
         patch("app.services.polish_service.memory_service.build_memory_context",
               new=AsyncMock(return_value="")), \
         patch("app.services.polish_service._client") as mock_client, \
         patch("app.services.polish_service.log_ai_call",
               new=AsyncMock(return_value=None)) as mock_log:
        mock_client.messages.create = AsyncMock(side_effect=RuntimeError("boom"))
        result = await svc.generate_weekly_review_email("u")

    assert result["subject"].startswith("Felix weekly review")
    assert "<!doctype html>" in result["html"]
    assert "couldn't generate the full briefing" in result["html"]
    assert "37 emails" in result["html"]
    # Plaintext alternative is derived from HTML
    assert "<" not in result["text"]
    assert "37 emails" in result["text"]
    # Failure was logged to ai_calls with success=False
    mock_log.assert_awaited_once()
    assert mock_log.await_args.kwargs["success"] is False
    assert mock_log.await_args.kwargs["feature"] == "weekly_review"


